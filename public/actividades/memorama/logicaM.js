let nivelesDesbloqueados = [1];

async function cargarNivelesDesbloqueados () {
  const url = `/niveles-desbloqueados?id_actividad=${ID_ACTIVIDAD_MEMORAMA}`
            + `&categoria=${encodeURIComponent(categoriaActual)}`;
  try {
    const r = await fetch(url, { credentials:'include' });
    const { niveles } = await r.ok ? await r.json() : { niveles:[1] };
    nivelesDesbloqueados = Array.isArray(niveles) ? niveles.map(Number) : [1];
  } catch { nivelesDesbloqueados = [1]; }
  actualizarBotonesNiveles();
}

async function desbloquearNiveles () {
  if (nivelActual < 3) {
    const sig = nivelActual + 1;
    if (!nivelesDesbloqueados.includes(sig)) await desbloquearNivel(sig);
  } else {
    const i = categoriasOrden.indexOf(categoriaActual);
    if (i !== -1 && i < categoriasOrden.length - 1)
      await desbloquearNivel(1, categoriasOrden[i + 1]);
  }
  await cargarNivelesDesbloqueados();
}

async function desbloquearNivel (nivel, cat = categoriaActual) {
  await fetch('/desbloquear-nivel', {
    method:'POST',
    credentials:'include',
    headers:{ 'Content-Type':'application/json' },
    body:JSON.stringify({
      id_actividad: ID_ACTIVIDAD_MEMORAMA,
      categoria   : cat,
      nivel       : Number(nivel)
    })
  });
}

