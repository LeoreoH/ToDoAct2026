async function guardarPuntuacion () {
  if (!localStorage.getItem('usuario')) return;
  await fetch('/guardar-progreso', {
    method:'POST',
    credentials:'include',
    headers:{ 'Content-Type':'application/json' },
    body:JSON.stringify({
      id_actividad      : ID_ACTIVIDAD_MEMORAMA,
      categoria         : categoriaActual,
      dificultad        : nivelActual,
      puntuacion        : puntuacion,
      tiempoFinalizacion: tiempoTranscurrido,
      erroresCometidos  : erroresCometidos
    })
  });
}


function volverANiveles() {
  juegoActivo = false; 
  if (temporizador) {
    clearInterval(temporizador);
    temporizador = null;
  }

  const mensajeVictoria = document.querySelector(".mensaje-victoria");
  const mensajeTiempo = document.querySelector(".mensaje-tiempo-agotado");
  if (mensajeVictoria) mensajeVictoria.remove();
  if (mensajeTiempo) mensajeTiempo.remove();

  document.getElementById("juego").style.display = "none";
  document.getElementById("niveles").style.display = "block";
  
  reiniciarJuego();
}

function reiniciarJuego() {
  const tabla = document.getElementById("tabla-memorama");
  tabla.innerHTML = "";
  juegoActivo = false;
  celdasReveladas = [];
  paresEncontrados = 0;
  puntuacion = 0;
  tiempoInicio = null;
  tiempoTranscurrido = 0;
  erroresCometidos = 0;
  bloquearClics = false;
  document.getElementById("puntuacion").textContent = "0";
  document.getElementById("tiempo-restante").textContent = "0";

  if (temporizador) {
    clearInterval(temporizador);
    temporizador = null;
  }
}

function navegarAtras() {
    if (temporizador) {
        clearInterval(temporizador);
        temporizador = null;
    }

    if (document.getElementById("juego").style.display === "block") {
        volverANiveles();
    } else if (document.getElementById("niveles").style.display === "block") {
        volverACategorias();
    } else {
        volverAInicio();
    }
}

